        public BlueprintTemplate blueprintTemplate { get; private set; }

        // dummy used to ensure that the static constructor has run
        public Blueprint() : this(-1, "", "", -1)
        { }

        private Blueprint(long? blueprintId, string edname, string templateEdName, int grade) :
            base(edname, edname.Replace("Sensor_Sensor_", "Sensor_").Replace("_", "").Replace(" ", "").Replace("1", "").Replace("2", "").Replace("3", "").Replace("4", "").Replace("5", ""))
        {
            this.grade = grade;
            this.blueprintId = blueprintId;
            blueprintTemplate = BlueprintTemplate.FromEDName(templateEdName);
            if (blueprintId != null)
            {
                BlueprintsByEliteID.Add((long)blueprintId, this);
            }
        }

        public static Blueprint FromEliteID(long eliteID, Dictionary<string, object> rawData = null)
        {
            if (eliteID <= 0) { return null; }
            BlueprintsByEliteID.TryGetValue(eliteID, out Blueprint blueprint);
            if (blueprint == null)
            {
                // Unknown module; report the full object so that we can update the definitions
                Logging.Error("Unknown blueprint Elite ID: " + eliteID, rawData);
                if (rawData != null)
                {
                    var edname = JsonParsing.getString(rawData, "BlueprintName");
                    var level = JsonParsing.getOptionalInt(rawData, "Level") ?? 1;
                    blueprint = new Blueprint(eliteID, edname.Replace("_", "") + level, edname.Replace("_", ""), level);
                }
            }
            return blueprint;
        }

        public static Blueprint FromEDNameAndGrade(string edname, int grade)
        {
            if (string.IsNullOrEmpty(edname)) { return null; }
            string tidiedEdName = edname.Replace("_", "");
            if (int.TryParse(tidiedEdName.Last().ToString(), out int _))
            {
                return FromEDName(tidiedEdName);
            }
            else
            {
                // Might be a blueprint template
                if (BlueprintTemplate.AllOfThem.Any(t => string.Equals(t.edname, tidiedEdName, StringComparison.InvariantCultureIgnoreCase)))
                {
                    return new Blueprint(null, tidiedEdName, tidiedEdName, grade);
                }
                else
                {
                    return null;
                }
            }
        }

        public static Blueprint FromNameAndGrade(string name, int grade)
        {
            if (string.IsNullOrEmpty(name)) { return null; }
            Blueprint result = FromName(name);
            // Since multiple blueprint grades share the same name, we may not have the correct blueprint yet.
            // We should, however, now have an edname that we can mutate into the correct grade.
            if (result != null && result.grade != grade)
            {
                result = FromEDNameAndGrade(result.edname.Replace(result.grade.ToString(), grade.ToString()), grade);
            }
            return result;
        }
